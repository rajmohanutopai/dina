import type { Metrics } from '@/shared/utils/metrics.js'

// ── Context ─────────────────────────────────────────────────────────

/** Shared context passed to every record handler */
export interface HandlerContext {
  db: DrizzleDB
  logger: Logger
  metrics: Metrics
}

// ── Record operation ────────────────────────────────────────────────

/** A single record operation from the Jetstream firehose */
export interface RecordOp {
  /** AT Protocol URI (at://did/collection/rkey) */
  uri: string
  /** DID of the record author */
  did: string
  /** Collection NSID (e.g. com.dina.trust.attestation) */
  collection: string
  /** Record key within the collection */
  rkey: string
  /** CID of the record (present on create/update) */
  cid?: string
  /** The record data (present on create/update, absent on delete) */
  record?: Record<string, unknown>
  /**
   * Synthetic correlation id (TN-OBS-002 / Plan §13.8). Generated by
   * the dispatcher at the top of `processEvent` so every downstream
   * log line — validation, rate-limit gate, handler write, dirty-flag
   * upsert — can be joined post-hoc by trace. Stored on
   * `attestations.trace_id` for record-level lookup. Optional because
   * the deletion path doesn't generate one (deletes have no row to
   * stamp), and tests sometimes drive handlers directly without the
   * dispatcher.
   */
  traceId?: string
}

// ── Handler interface ───────────────────────────────────────────────

/** Interface that every record handler must implement */
export interface RecordHandler {
  /** Handle a record creation or update */
  handleCreate(ctx: HandlerContext, op: RecordOp): Promise<void>
  /** Handle a record deletion */
  handleDelete(ctx: HandlerContext, op: RecordOp): Promise<void>
}

